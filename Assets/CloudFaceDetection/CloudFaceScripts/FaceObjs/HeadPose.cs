using System;

/// <summary>
    /// The head pose entity.
    /// </summary>
[Serializable]
public class HeadPose
{
	
    /// <summary>
    /// Gets or sets the roll.
    /// </summary>
    /// <value>
    /// The roll of the face pose.
    /// </value>
	public float roll;

    /// <summary>
    /// Gets or sets the yaw.
    /// </summary>
    /// <value>
    /// The yaw of the face pose.
    /// </value>
	public float yaw;

    /// <summary>
    /// Gets or sets the pitch.
    /// </summary>
    /// <value>
    /// The pitch of the face pose.
    /// </value>
	public float pitch;

}
