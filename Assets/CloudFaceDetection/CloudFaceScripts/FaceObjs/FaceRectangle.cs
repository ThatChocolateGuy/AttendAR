using System;

/// <summary>
/// The face rectangle entity.
/// </summary>
[Serializable]
public class FaceRectangle
{
	
    /// <summary>
    /// Gets or sets the width.
    /// </summary>
    /// <value>
    /// The width.
    /// </value>
	public int width;

    /// <summary>
    /// Gets or sets the height.
    /// </summary>
    /// <value>
    /// The height.
    /// </value>
	public int height;

    /// <summary>
    /// Gets or sets the left.
    /// </summary>
    /// <value>
    /// The left of the face rectangle.
    /// </value>
	public int left;

    /// <summary>
    /// Gets or sets the top.
    /// </summary>
    /// <value>
    /// The top of the face rectangle.
    /// </value>
	public int top;

}

